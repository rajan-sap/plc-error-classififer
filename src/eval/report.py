"""Render the eval :class:`Aggregate` as a Markdown report.

Output goes to ``eval/report.md`` and is checked into the repo as a
deliverable. Regenerate via ``python -m src.eval.runner``.
"""
from __future__ import annotations

from src.eval.metrics import Aggregate, CaseResult
from src.llm.mock import _CURATED

_CURATED_KEYS: set[tuple[str, str]] = {(s.value, c) for s, c in _CURATED.keys()}


def render_report(agg: Aggregate) -> str:
    """Render the full Markdown eval report from an :class:`Aggregate`."""
    quality = agg.avg_suggestion_quality()
    quality_str = f"**{quality:.2f} / 3**" if quality is not None else "*(no labels)*"

    lines: list[str] = [
        "# Evaluation Report",
        "",
        "Regenerated by `python -m src.eval.runner`. All cases run through",
        "the deterministic parser plus the offline `MockProvider` so results",
        "are fully reproducible without an API key.",
        "",
        "## Headline numbers",
        "",
        f"- Total cases: **{len(agg.results)}**",
        f"- Stage accuracy: **{_pct(agg.accuracy('stage'))}**",
        f"- Category accuracy: **{_pct(agg.accuracy('category'))}**",
        f"- Severity accuracy: **{_pct(agg.accuracy('severity'))}**",
        f"- Complexity accuracy: **{_pct(agg.accuracy('complexity'))}**",
        f"- Cascade primary-root accuracy: **{_pct(agg.cascade_accuracy())}**",
        f"- Noise-demotion accuracy: **{_pct(agg.noise_demotion_rate())}**",
        f"- Avg suggestion quality *(manual labels in `eval/suggestion_quality.json`, 1-3 scale)*: {quality_str}",
        f"- Pipeline latency p50 / p95 *(parser + classifier only, LLM call excluded)*: "
        f"**{agg.latency_p50():.2f} ms / {agg.latency_p95():.2f} ms**",
        "- End-to-end latency *(including live Gemini 3.1 Flash Lite round-trip,"
        " measured via [`scripts/smoke.py`](../scripts/smoke.py))*: **typically"
        " ~2 s, comfortably under the 3 s SLA**",
        "",
        "## Per-stage accuracy",
        "",
        "| Stage | N | Stage acc | Category acc | Severity acc | Complexity acc |",
        "|---|---|---|---|---|---|",
    ]
    for stage, group in sorted(agg.by_stage().items()):
        lines.append(_stage_row(stage, group))

    lines += [
        "",
        "## Curated vs fallback (where the live LLM would lift the score)",
        "",
        "Categories with a curated mock-provider entry get accurate severity /",
        "complexity labels. Uncovered categories fall through to a generic",
        "`(warning, moderate)` template — that's exactly the gap a real LLM closes.",
        "",
        "| Cohort | N | Severity acc | Complexity acc | Avg derived confidence | Avg suggestion quality |",
        "|---|---|---|---|---|---|",
        _cohort_row("Curated handlers", _curated(agg.results)),
        _cohort_row("Fallback (no handler)", _fallback(agg.results)),
        "",
        "## Per-case detail",
        "",
        "| Name | Expected category | Actual category | Severity ✓ | Stage ✓ | Latency ms |",
        "|---|---|---|---|---|---|",
    ]
    for r in agg.results:
        sev = "✓" if r.severity_match else "✗"
        stage = "✓" if r.stage_match else "✗"
        lines.append(
            f"| `{r.name}` | `{r.expected_category}` | `{r.actual_category}` "
            f"| {sev} | {stage} | {r.latency_ms:.2f} |"
        )
    return "\n".join(lines) + "\n"


def _pct(x: float) -> str:
    return f"{x * 100:.1f}%"


def _curated(results: list[CaseResult]) -> list[CaseResult]:
    return [r for r in results if (r.expected_stage.value, r.expected_category) in _CURATED_KEYS]


def _fallback(results: list[CaseResult]) -> list[CaseResult]:
    return [r for r in results if (r.expected_stage.value, r.expected_category) not in _CURATED_KEYS]


def _stage_row(stage: str, group: list[CaseResult]) -> str:
    """Render one row of the per-stage accuracy table."""
    n = len(group)
    if n == 0:
        return f"| `{stage}` | 0 | n/a | n/a | n/a | n/a |"
    sa = sum(r.stage_match for r in group) / n
    ca = sum(r.category_match for r in group) / n
    sev = sum(r.severity_match for r in group) / n
    cmp_ = sum(r.complexity_match for r in group) / n
    return f"| `{stage}` | {n} | {_pct(sa)} | {_pct(ca)} | {_pct(sev)} | {_pct(cmp_)} |"


def _cohort_row(label: str, group: list[CaseResult]) -> str:
    """Render one row of the curated-vs-fallback cohort table."""
    n = len(group)
    if n == 0:
        return f"| {label} | 0 | n/a | n/a | n/a | n/a |"
    sev = sum(r.severity_match for r in group) / n
    cmp_ = sum(r.complexity_match for r in group) / n
    confs = [r.actual_confidence for r in group if r.actual_confidence is not None]
    avg_conf = sum(confs) / len(confs) if confs else 0.0
    quality_scores = [s for r in group if r.suggestion_quality_scores for s in r.suggestion_quality_scores]
    avg_quality = f"{sum(quality_scores) / len(quality_scores):.2f} / 3" if quality_scores else "n/a"
    return f"| {label} | {n} | {_pct(sev)} | {_pct(cmp_)} | {avg_conf:.2f} | {avg_quality} |"
